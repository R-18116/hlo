# sometimes arguments and parameters are same
# in def 'a' is argument and 'b' is parameter
# 'a'  is indeed an argument because it's a placeholder for the value that will be 
#provided when calling the add function.
# 'b' is a parameter because it's a variable defined in the function definition.
def add(a,b):
    c=a+b
    print(c)
add(2,4)    